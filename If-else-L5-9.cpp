//tell whether given alphabet is vowel or consonent using if-else and logical operators.

#include <iostream>
using namespace std;

int main()
{
    char alphabet;
    cin>>alphabet;

    if (alphabet=='a'){
        cout<<"vowel";
    }
    else if (alphabet=='e'){
        cout<<"vowel";
    }
    else if (alphabet=='i'){
        cout<<"vowel";
    }
    else if (alphabet=='o'){
        cout<<"vowel";
    }
    else if (alphabet=='u'){
        cout<<"vowel";
    }
    else {
        cout<<"consonant";
    }

}